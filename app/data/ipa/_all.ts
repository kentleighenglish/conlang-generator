const filterCollection = <T extends IPAConsonant | IPAVowel>(
  selection: string[],
  collection: T[],
  ipaType: string
) =>
  selection.reduce((acc: T[], selectionLigature) => {
    const found = collection.find((ipa) => ipa.ligature === selectionLigature);
    if (!found) {
      console.error(`Cannot find ${ipaType} for ${selectionLigature}`);
    } else {
      acc.push(found);
    }
    return acc;
  }, [] as T[]);

export const getVowelCollection = (...selection: string[]): IPAVowel[] =>
  filterCollection<IPAVowel>(selection, vowels, "vowel");

export const getConsonantCollection = (
  ...selection: string[]
): IPAConsonant[] =>
  filterCollection<IPAConsonant>(selection, consonants, "consonant");
